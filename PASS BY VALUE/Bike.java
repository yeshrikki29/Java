class Bike {
    int c;
    String bc;
    String bn;

    Bike(int p, String q, String r) {
        c = p;
        bc = q;
        bn = r;
    }

    public static void main(String[] y) {
        Bike i = new Bike(479, "wdcdv", "dwqac");
        System.out.println(i.c);
        System.out.println(i.bc);
        System.out.println(i.bn);

    }
}