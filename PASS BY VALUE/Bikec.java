class Bikec {
    int c;
    String bc;
    String bn;

    Bikec(int c, String bc, String bn) {
        this.c = c;
        this.bc = bc;
        this.bn = bn;
    }

    public static void main(String[] y) {
        Bikec i = new Bikec(479, "wdcdv", "dwqac");
        System.out.println(i.c);
        System.out.println(i.bc);
        System.out.println(i.bn);

    }
}